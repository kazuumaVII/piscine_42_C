#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	unsigned int	i;

	i = nb;
	if (nb < 0)
	{
		i = -nb;
		ft_putchar('-');
	}
	if (i > 9)
		ft_putnbr(i / 10);
	ft_putchar(i % 10 + 48);
}
