int	ft_atoi(const char *str)
{
	int	i;
	int	t;
	int	neg;

	i = 0;
	t = 0;
	neg = 1;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -neg;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		t = t * 10 + (str[i] - 48);
		i++;
	}
	return (t * neg);
}
